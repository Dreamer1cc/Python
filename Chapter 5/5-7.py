# в списке . 
# • Создайте список трех своих любимых фруктов и назовите его favorite_fruits . 
# • Напишите пять команд if . Каждая команда должна проверять, входит ли 
# определенный тип фрукта в список . 
# Если фрукт входит в список, блок if должен выводить сообщение 
# вида «You really like bananas!» .

favorite_fruits = ["бананы", "яблоки", "клубника"]
if "бананы" in favorite_fruits:
    print("You really like bananas!")

if "яблоки" in favorite_fruits:
    print("You really like apples!")

if "черешня" in favorite_fruits:
    print("You really like черешню!")

if "алыча" in favorite_fruits:
    print("You really like алычу!")

